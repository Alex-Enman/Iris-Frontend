export type Language = 'en' | 'sv';

export const LANGUAGE_STORAGE_KEY = 'iris-ui-language' as const;

export const DEFAULT_LANGUAGE: Language = 'en';

export function normalizeLanguage(value: unknown): Language {
  return value === 'sv' ? 'sv' : 'en';
}

export function getStoredLanguage(
  storageKey: string = LANGUAGE_STORAGE_KEY,
  fallback: Language = DEFAULT_LANGUAGE
): Language {
  if (typeof window === 'undefined') return fallback;
  return normalizeLanguage(window.localStorage.getItem(storageKey));
}

export function languageToLocale(language: Language): string {
  return language === 'sv' ? 'sv-SE' : 'en-US';
}

export function getCurrentLocale(
  storageKey: string = LANGUAGE_STORAGE_KEY,
  fallback: Language = DEFAULT_LANGUAGE
): string {
  return languageToLocale(getStoredLanguage(storageKey, fallback));
}

